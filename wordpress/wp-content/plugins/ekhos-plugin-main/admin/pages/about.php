            <h1 class="text-2xl font-bold text-base-100">À Propos du Plugin IdSound pour WordPress</h1>

            <h2 class="text-xl font-semibold text-base-100">Introduction</h2>
            <p class="text-base">
                <strong>IdSound</strong> est un plugin innovant pour WordPress qui enrichit l'expérience utilisateur en
                intégrant une dimension sonore interactive sur votre site web. Ce plugin permet aux administrateurs de
                site de déployer des effets sonores qui se déclenchent en réponse à diverses actions des utilisateurs,
                transformant ainsi la navigation sur le site en une expérience immersive et dynamique.
            </p>

            <h2 class="text-xl font-semibold text-base-100">Fonctionnalités Principales</h2>
            <p class="text-base">IdSound offre une gestion simplifiée mais puissante des sons à travers trois interfaces
                principales :</p>
            <ol class="list-decimal pl-4 text-base">
                <li>
                    <strong>Personnages :</strong>
                    <ul class="list-disc pl-5 text-base">
                        <li><strong>Ajout de Personnages :</strong> Créez et gérez des personnages qui seront associés à
                            des sons spécifiques. Chaque personnage peut se voir attribuer un son par défaut, évoquant
                            leur "voix" ou effet sonore caractéristique lors de certaines interactions.
                        </li>
                        <li><strong>Son par Défaut :</strong> Attribuez un son par défaut à chaque personnage pour une
                            interaction spécifique, ce qui facilite la gestion et l'attribution des sons sur votre site.
                        </li>
                    </ul>
                </li>
                <li>
                    <strong>Sons :</strong>
                    <ul class="list-disc pl-5 text-base">
                        <li><strong>Importation de Sons :</strong> Téléchargez et stockez une bibliothèque de sons qui
                            peuvent être attribués à différents personnages ou actions sur le site.
                        </li>
                        <li><strong>Association avec un Personnage :</strong> Liez chaque son importé à un personnage
                            spécifique pour maintenir une organisation claire et accessible.
                        </li>
                    </ul>
                </li>
                <li>
                    <strong>Lier des Sons :</strong>
                    <ul class="list-disc pl-5 text-base">
                        <li><strong>Association Son-Interaction :</strong> Configurez des associations spécifiques entre
                            les sons et les interactions des utilisateurs, telles que le clic sur un bouton, le survol
                            d'un lien, ou le défilement à travers une section spécifique du site.
                        </li>
                        <li><strong>Sélecteur HTML :</strong> Utilisez un sélecteur HTML pour attacher des sons à des
                            éléments spécifiques, permettant une grande flexibilité et personnalisation.
                        </li>
                    </ul>
                </li>
            </ol>

            <h2 class="text-xl font-semibold text-base-100">Interaction Utilisateur</h2>
            <p class="text-base">
                Les sons sur IdSound sont déclenchés par des actions telles que :
            <ul class="list-disc pl-5 text-base">
                <li><strong>Clics :</strong> Activez des sons lors du clic sur des éléments comme les boutons et les
                    liens.
                </li>
                <li><strong>Changement de Section :</strong> Jouez des effets sonores au défilement à mesure que
                    l'utilisateur navigue entre différentes sections du site.
                </li>
                <li><strong>Événements Automatiques :</strong> Configurez des sons pour se lancer automatiquement lors
                    du chargement des pages ou lors de l'apparition de pop-ups.
                </li>
            </ul>
            </p>

